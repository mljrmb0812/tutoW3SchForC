#include <stdio.h>

int main() {
	// Create variables
	// Créer les variables
	int monNombre = 6;	// Integer/Entier (whole number°
	float monNombreFlottant = 7.32;	// Nombre à virgule flottante / Floating point number
	char maLettre = 'F'; // Caractère ou lettre

	// Print variables
	// Imprimer les variables
	printf("%d\n", monNombre);
	printf("%f\n", monNombreFlottant);
	printf("%c\n", maLettre);
	return 0;
}
