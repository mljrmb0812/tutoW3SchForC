#include <stdio.h>

int main() {
	int jour = 3;

	switch (jour) {
		case 1:
			printf("Lundi/Monday");
			break;
		case 2:
			printf("Mardi/Monday");
			break;
		case 3:
			printf("Mercredi/Wednesday");
			break;
		case 4:
			printf("Jeudi/Thursday");
			break;
		case 5:
			printf("Vendredi/Friday");
			break;
		case 6:
			printf("Samedi/Saturday");
			break;
		case 7:
			printf("Dimanche/Sunday");
			break;
		}
	return 0;
}